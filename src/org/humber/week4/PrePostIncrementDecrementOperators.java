package org.humber.week4;

public class PrePostIncrementDecrementOperators {


    public static void main(String[] args) {
        int num = 9;

        System.out.println("num: " + num);
        System.out.println("num++: " + (num++));

        System.out.println("num: " + num);
        System.out.println("++num: " + (++num));

        System.out.println("num: " + num);
        System.out.println("num--: " + (num--));

        System.out.println("num: " + num);
        System.out.println("--num: " + (--num));

        System.out.println("num: " + num);



    }


}
